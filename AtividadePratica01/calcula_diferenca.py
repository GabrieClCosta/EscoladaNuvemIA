A = int(input("Digite o valor de A: "))
B = int(input("Digite o valor de B: "))
C = int(input("Digite o valor de C: "))
D = int(input("Digite o valor de D: "))

diferenca = (A * B) - (C * D)

print("A formula para calcular a diferença é: (A * B) - (C * D)")
print(f"A diferença entre o produto de {A} e {B} e o produto de {C} e {D} é {diferenca}.")